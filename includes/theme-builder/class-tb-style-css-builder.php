	/**
	 * Load an archetype CSS file and return its contents, stripping the file
	 * header comment so it embeds cleanly inside the generated style.css.
	 */
	private static function load_archetype( string $slug ): string {
		$path = self::ARCHETYPES_DIR . $slug . '.css';
		if ( ! file_exists( $path ) ) {
			return "/* archetype '{$slug}' not found — using Bootstrap defaults */";
		}
		$css = file_get_contents( $path );
		// Strip the leading /* ... */ file-header comment block.
		$css = preg_replace( '/^\/\*[\s\S]*?\*\/\s*/m', '', $css, 1 );
		return trim( $css );
	}

	/**
	 * Return "R,G,B" string from a hex color for use in rgba().
	 */
	private static function hex_to_rgb( string $hex ): string {
		$h = ltrim( $hex, '#' );
		if ( strlen( $h ) === 3 ) {
			$h = $h[0] . $h[0] . $h[1] . $h[1] . $h[2] . $h[2];
		}
		$r = hexdec( substr( $h, 0, 2 ) );
		$g = hexdec( substr( $h, 2, 2 ) );
		$b = hexdec( substr( $h, 4, 2 ) );
		return "{$r},{$g},{$b}";
	}

