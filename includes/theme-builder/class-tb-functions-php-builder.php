	/**
	 * Build a wp_enqueue_style() call line.
	 */
	private static function enqueue_style_line( string $slug, string $handle, string $path, string $version ): string {
		ob_start(); ?>
	wp_enqueue_style(
		'<?php echo $slug; ?>-<?php echo $handle; ?>',
		get_template_directory_uri() . '<?php echo $path; ?>',
		array(),
		'<?php echo $version; ?>'
	);
		<?php
		return ob_get_clean() . "\n";
	}

	/**
	 * Download Bootstrap CSS/JS into the generated theme's assets/ directory.
	 *
	 * Called by GrayFox_ThemeBuilder after all theme files are written.
	 * Fetches from CDN once at theme-generation time so the theme itself
	 * serves Bootstrap locally — satisfying WP Guideline 8.
	 *
	 * @param string $theme_dir    Absolute path to the generated theme directory.
	 * @param bool   $include_icons Whether to also download Bootstrap Icons CSS.
	 * @return bool True on success, false if any download failed.
	 */
	public static function copy_bootstrap_assets( string $theme_dir, bool $include_icons = false ): bool {
		$bs_ver  = self::BOOTSTRAP_VERSION;
		$bi_ver  = self::BOOTSTRAP_ICONS_VERSION;
		$css_dir = $theme_dir . '/assets/css';
		$js_dir  = $theme_dir . '/assets/js';

		wp_mkdir_p( $css_dir );

		$downloads = array(
			$css_dir . '/bootstrap.min.css'        => 'https://cdn.jsdelivr.net/npm/bootstrap@' . $bs_ver . '/dist/css/bootstrap.min.css',
			$js_dir  . '/bootstrap.bundle.min.js'  => 'https://cdn.jsdelivr.net/npm/bootstrap@' . $bs_ver . '/dist/js/bootstrap.bundle.min.js',
		);

		if ( $include_icons ) {
			$downloads[ $css_dir . '/bootstrap-icons.min.css' ] = 'https://cdn.jsdelivr.net/npm/bootstrap-icons@' . $bi_ver . '/font/bootstrap-icons.min.css';
		}

		foreach ( $downloads as $dest => $url ) {
			$response = wp_remote_get( $url, array( 'timeout' => 20 ) );
			if ( is_wp_error( $response ) || 200 !== wp_remote_retrieve_response_code( $response ) ) {
				if ( defined( 'WP_DEBUG' ) && WP_DEBUG ) {
					error_log( 'GrayFox Theme Builder: failed to download Bootstrap asset: ' . $url ); // phpcs:ignore WordPress.PHP.DevelopmentFunctions.error_log_error_log
				}
				return false;
			}
			global $wp_filesystem;
			if ( $wp_filesystem ) {
				$wp_filesystem->put_contents( $dest, wp_remote_retrieve_body( $response ), FS_CHMOD_FILE );
			} else {
				// phpcs:ignore WordPress.WP.AlternativeFunctions.file_system_operations_file_put_contents
				file_put_contents( $dest, wp_remote_retrieve_body( $response ) );
			}
		}

		return true;
	}

	/**
	 * Build a wp_enqueue_script() call line (footer).
	 */
	private static function enqueue_script_line( string $slug, string $handle, string $path, string $version ): string {
		ob_start(); ?>
	wp_enqueue_script(
		'<?php echo $slug; ?>-<?php echo $handle; ?>',
		get_template_directory_uri() . '<?php echo $path; ?>',
		array(),
		'<?php echo $version; ?>',
		true
	);
		<?php
		return ob_get_clean() . "\n";
	}
}
